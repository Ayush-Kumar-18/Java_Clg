public class Method_DatatypeOfParameters {
    public static void main(String[] args) {
        System.out.println("Integer method: "); display(3);
        System.out.println("Float method: "); display(1.233f);
        System.out.println("Char method: "); display('m');
        System.out.println("String method: "); display("Ayush Kumar");
    }
    static void display(int a){
        System.out.println(a);
    }
    static void display(float a){
        System.out.println(a);
    }
    static void display(char a) {
        System.out.println(a);
    }
    static void display(String a){
        System.out.println(a);
    }
}
